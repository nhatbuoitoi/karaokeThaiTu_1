    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblDanhSach.getModel();
        DanhMucDAO dmDAO = new DanhMucDAO();
        try {
            int madanhmuc = Integer.parseInt(txtTimDm.getText()); // Lấy mã tài khoản từ JTextField
           Danh_Muc dm = dmDAO.timDanhMuc(Integer.parseInt(txtTimDm.getText())); // Gọi phương thức tìm kiếm
          
            model.setRowCount(0); // Xóa tất cả các hàng trong bảng

            if (dm != null) {
                // Thêm tài khoản tìm thấy vào bảng
                model.addRow(new Object[]{
                dm.getMA_DANH_MUC(),
                dm.getTEN_DANH_MUC() // <-- sửa dòng này
});

            } else {
                // Hiển thị thông báo không tìm thấy tài khoản
                
                JOptionPane.showMessageDialog(this, "Không tìm thấy danh mục với mã: " + madanhmuc);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã danh mục hợp lệ.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

