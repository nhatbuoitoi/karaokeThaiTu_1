      public void loadTable() {
        DanhMucDAO dmDAO = new DanhMucDAO();
        List<Danh_Muc> dmLst = dmDAO.readDanhMuc();
        DefaultTableModel tblDanhSach = (DefaultTableModel) this.tblDanhSach.getModel();
        tblDanhSach.setRowCount(0);
        for (Danh_Muc dm : dmLst) {
            tblDanhSach.addRow(new Object[]{
                dm.getMA_DANH_MUC(),
                dm.getTEN_DANH_MUC()
            });
        }

    }
      private boolean validateInput() {
    if (txtmaDanhMuc.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Mã danh mục không được để trống!");
        txtmaDanhMuc.requestFocus();
        return false;
    }
    if (txttenDanhMuc.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Tên danh mục không được để trống!");
        txttenDanhMuc.requestFocus();
        return false;
    }
    return true;
}

