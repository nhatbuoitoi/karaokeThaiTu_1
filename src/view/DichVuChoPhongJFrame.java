    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        DefaultTableModel modelP = (DefaultTableModel) tblDanhSachDichVu.getModel();
        DefaultTableModel modelT = (DefaultTableModel) tblDanhSachDichVuDaThem.getModel();

        for (int i = modelT.getRowCount() - 1; i >= 0; i--) {
            if (modelT.getValueAt(i, 0) == null) {
                modelT.removeRow(i);
            }
        }

        int maDV = Integer.parseInt(modelP.getValueAt(tblDanhSachDichVu.getSelectedRow(), 0).toString());
        String tenDV = modelP.getValueAt(tblDanhSachDichVu.getSelectedRow(), 1).toString();
        double donGia = Double.parseDouble(modelP.getValueAt(tblDanhSachDichVu.getSelectedRow(), 4).toString());

        for (int i = 0; i < modelT.getRowCount(); i++) {
            if (modelT.getValueAt(i, 0) == null) continue; // Bỏ qua dòng trống

            if (Integer.parseInt(modelT.getValueAt(i, 0).toString()) == maDV) {
                modelT.setValueAt((int) modelT.getValueAt(i, 2) + 1, i, 2);
                modelT.setValueAt((int) modelT.getValueAt(i, 2) * donGia, i, 4);
                tinhTongTien();
                return;
            }
        }

        // Thêm mới nếu không tìm thấy
        modelT.addRow(new Object[]{maDV, tenDV, 1, donGia, donGia});
        tinhTongTien();
    }//GEN-LAST:event_jButton3ActionPerformed

