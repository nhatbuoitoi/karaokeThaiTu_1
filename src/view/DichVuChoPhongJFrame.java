    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int row = tblDanhSachDichVuDaThem.getSelectedRow();
        if (row == -1) return;

        DefaultTableModel modelT = (DefaultTableModel) tblDanhSachDichVuDaThem.getModel();
        int soLuong = (int) modelT.getValueAt(row, 2);

        if (--soLuong > 0) {
            modelT.setValueAt(soLuong, row, 2);
            modelT.setValueAt(soLuong * (double) modelT.getValueAt(row, 3), row, 4);
        } else modelT.removeRow(row);

        tinhTongTien();
    }//GEN-LAST:event_jButton5ActionPerformed

