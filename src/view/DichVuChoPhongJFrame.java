    private void tinhTongTien() {
        DefaultTableModel modelT = (DefaultTableModel) tblDanhSachDichVuDaThem.getModel();
        double tongTien = 0;
        for (int i = 0; i < modelT.getRowCount(); i++) {
            tongTien += Double.parseDouble(modelT.getValueAt(i, 4).toString());
        }
        txtTongTien.setText(String.valueOf(tongTien));
    }
    
    public void xacNhanDichVu(int maHoaDon) {

        DefaultTableModel modelT = (DefaultTableModel) tblDanhSachDichVuDaThem.getModel();

        String sql = "insert into CHI_TIET_DICH_VU(MA_HOA_DON, MA_DICH_VU, SO_LUONG, DON_GIA)\n" +
                     "values (?, ?, ?, ?)";
        
        try (Connection con = KetNoiDB.getConnection(); 
            PreparedStatement ps = con.prepareStatement(sql);) {
                    
            
            for(int i = 0; i < modelT.getRowCount(); i++) {
                
                int maDichVu = Integer.parseInt(modelT.getValueAt(i, 0).toString());
                int soLuong = Integer.parseInt(modelT.getValueAt(i, 2).toString());
                double donGia = Double.parseDouble(modelT.getValueAt(i, 3).toString());
                
                ps.setInt(1, this.maHoaDon);
                ps.setInt(2, maDichVu);
                ps.setInt(3, soLuong);
                ps.setDouble(4, donGia);
                ps.addBatch();
            }
        
            ps.executeBatch();
            JOptionPane.showMessageDialog(null, "Đặt dịch vụ thành công!");
            modelT.setRowCount(0); 
        
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Lỗi khi đặt dịch vụ!");
        }
    }
    
    
    
    
