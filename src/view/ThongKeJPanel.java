    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

            String ngayBatDauStr = txtNgayBD.getText();
            String ngayKetThucStr = txtNgayKT.getText();

            labelBD.setText(ngayBatDauStr);
            labelKT.setText(ngayKetThucStr);

            Date ngayBatDau = sdf.parse(ngayBatDauStr);
            Date ngayKetThuc = sdf.parse(ngayKetThucStr);

            ThongKeDAO tkDAO = new ThongKeDAO();
            List<Hoa_Don> danhSachHoaDon = tkDAO.loadHoaDon(new java.sql.Date(ngayBatDau.getTime()), new java.sql.Date(ngayKetThuc.getTime()));
            double tong = tkDAO.loadTong( new java.sql.Date(ngayBatDau.getTime()), new java.sql.Date(ngayKetThuc.getTime()) ) ;
            txtTongdt.setText(String.valueOf(tong));

            DefaultTableModel model = (DefaultTableModel) tblDanhSach.getModel();
            model.setRowCount(0);

            for (Hoa_Don hd : danhSachHoaDon) {
                String trangThaiThanhToanText = hd.isTRANG_THAI_THANH_TOAN() ? "Đã Thanh Toán" : "Chưa Thanh Toán";
                Object[] row = {
                    hd.getMA_HOA_DON(),
                    hd.getMA_KHACH_HANG(),
                    hd.getMA_TAI_KHOAN(),
                    hd.getNGAY_TAO(),
                    hd.getTONG_TIEN(),
                    trangThaiThanhToanText
                };
                model.addRow(row);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Lỗi tìm kiếm hóa đơn!");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnXacNhanActionPerformed


